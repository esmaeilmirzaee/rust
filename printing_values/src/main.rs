fn main() {
    println!("Hello, world!");
    println!("My name is {} and I am {} years old.", "Esmaeil", 35);
    println!("3 + 2 = {}", 3 + 2);
    println!("{0} doesn't have a {2} and a {1}.", "Esmaeil", "dog", "cat");
    println!("{first_name} {surname}", surname="Esmaeil", first_name="MIRZAEE");
    println!("Binary {:b}, Hexadecimal {:x} and Octal {:o}", 5, 5, 5);
    println!("Array: {:?}", [1, 2, 3, 4, 5]);
}
